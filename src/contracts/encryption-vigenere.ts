export function encryptVigenere(plaintext: string, key: string) {
    const alphabet = vigenereSquare[0];
    let keyIndex = 0;
    let encrypted = "";
    for (const char of plaintext) {
        if (alphabet.indexOf(char) === -1) {
            encrypted += char;
            continue;
        }
        const row = alphabet.indexOf(char);
        const col = alphabet.indexOf(key[keyIndex]);
        encrypted += vigenereSquare[col][row];
        keyIndex = (keyIndex + 1) % key.length;
    }
    return encrypted;
}